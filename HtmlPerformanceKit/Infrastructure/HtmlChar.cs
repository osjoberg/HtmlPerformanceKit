        internal static HtmlChar ConvertFromUtf32(int utf32)
        {
            if (utf32 < 0 || utf32 > 0x10ffff)
            {
                throw new ArgumentException("A valid UTF32 value is between 0x000000 and 0x10ffff, inclusive, and should not include surrogate codepoint values (0x00d800 ~ 0x00dfff)", nameof(utf32));
            }

            if (utf32 >= 0xd800 && utf32 <= 0x00dfff)
            {
                throw new ArgumentException("A valid UTF32 value is between 0x000000 and 0x10ffff, inclusive, and should not include surrogate codepoint values (0x00d800 ~ 0x00dfff)", nameof(utf32));
            }

            if (utf32 < 0x10000)
            {
                return new HtmlChar((char)utf32);
            }

            var inMinus0x10000 = utf32 - 0x10000;

            return new HtmlChar((char)((inMinus0x10000 / 0x400) + 0xd800), (char)((inMinus0x10000 % 0x400) + 0xdc00));
        }

        internal static bool IsCharacterReferenceReplacementToken(int codepoint)
        {
            return (codepoint >= 0xD800 && codepoint <= 0xDFFF) || codepoint >= 0x10FFFF;
        }

        internal static bool IsCharacterReferenceParseError(int codepoint)
        {
            if ((codepoint >= 0x0001 && codepoint <= 0x0008) ||
              (codepoint >= 0x000D && codepoint <= 0x001F) ||
                (codepoint >= 0x007F && codepoint <= 0x009F) ||
                (codepoint >= 0xFDD0 && codepoint <= 0xFDEF))
            {
                return true;
            }

            switch (codepoint)
            {
                case 0x000B:
                case 0xFFFE:
                case 0xFFFF:
                case 0x1FFFE:
                case 0x1FFFF:
                case 0x2FFFE:
                case 0x2FFFF:
                case 0x3FFFE:
                case 0x3FFFF:
                case 0x4FFFE:
                case 0x4FFFF:
                case 0x5FFFE:
                case 0x5FFFF:
                case 0x6FFFE:
                case 0x6FFFF:
                case 0x7FFFE:
                case 0x7FFFF:
                case 0x8FFFE:
                case 0x8FFFF:
                case 0x9FFFE:
                case 0x9FFFF:
                case 0xAFFFE:
                case 0xAFFFF:
                case 0xBFFFE:
                case 0xBFFFF:
                case 0xCFFFE:
                case 0xCFFFF:
                case 0xDFFFE:
                case 0xDFFFF:
                case 0xEFFFE:
                case 0xEFFFF:
                case 0xFFFFE:
                case 0xFFFFF:
                case 0x10FFFE:
                case 0x10FFFF:
                    return true;

                default:
                    return false;
            }
        }

        internal void CopyTo(char[] destination, int destinationIndex)
        {
            if (char1 == '\0')
            {
                return;
            }

            destination[destinationIndex++] = char1;

            if (char2 != '\0')
            {
                destination[destinationIndex] = char2;
            }
        }
